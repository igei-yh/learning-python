#!/usr/bin/env python

# "%"operator is used to format set of variables in tuple, together with format string.
# special symbols like "%s" and "%d".

string1 = "submarine"
print("Yellow %s !!" % string1)

string2 = "Yellow"
string3 = "Submarine"
integer1 = 1969
print("%s %s !! in %d" % (string2, string3, integer1))

list1 = [1,9,6,9]
print("Yellow Submarine !! %s" % list1)

# %s is string or any object.
# %d is integers.
# %f is float.

