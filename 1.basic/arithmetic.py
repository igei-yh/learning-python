#!/usr/bin/env python

# addition, subtraction, multiplication, and division operators can be used.
numbers = 1 + 2 * 3 / 4.0
print(numbers)

# "modulo(%)" operator which returns integer remainder of division. 
rem = 10 % 4
print(rem)

# Python can also concat strings.
string1 = "Help" + "any" + "somebady" + "!"
print(string1)

# Python also support multipl string.
string2 = "Help" * 10
print(string2)

# list can also be added.
even_list = [2,4,6,8]
odd_list = [1,3,5,7,9]
num_list = even_list + odd_list
print(num_list)
# sort is function for rearranging elements.
num_list.sort()
print(num_list)
