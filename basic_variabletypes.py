#!/usr/bin/env python

# Python is not statically typed.
# it is not necessary to declare variables before using them.
# Number
integer = 5
print(integer)

# float
# it can also be defined as float beforehand.
floatnumber = 2.0
print(floatnumber)

floatnumber = float(3)
print(floatnumber)

# strings
# string is enclosed ' or ".
string1 = 'hello'
print(string1)
string2 = "world"
print(string2)

print(string1 + string2)

# " encloses ' in the string.
string3 = "Hello's world"
print(string3)
